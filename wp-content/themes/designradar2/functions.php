	include_once('functions/ajax.inc.php');



	// ENQUEUE SCRIPTS
	
	function drt_scripts() {
	    wp_enqueue_script( 'jquery' );
	    wp_enqueue_script( 'less', get_bloginfo('template_directory') . '/js/less-1.1.3.min.js');
	}    
	 
	add_action('wp_enqueue_scripts', 'drt_scripts');

?>